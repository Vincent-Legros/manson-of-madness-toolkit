package org.amphiprion.mansionofmadness.comparator;

import java.util.Comparator;

import org.amphiprion.mansionofmadness.dto.Tile;

/**
 * @author Amphiprion
 * 
 */
public class TileComparator implements Comparator<Tile> {

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.util.Comparator#compare(java.lang.Object, java.lang.Object)
	 */
	@Override
	public int compare(Tile object1, Tile object2) {
		return object1.getDisplayName().compareToIgnoreCase(object2.getDisplayName());
	}

}
